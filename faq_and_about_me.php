            <details class="faq-item">
                <summary>Fällt eine Anzahlung an?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Ja – zur Fixierung des Termins erheben wir eine Anzahlung, die am Tattoo-Termin vollständig
                    angerechnet wird.
                    Der Termin wird <b>erst nach bestätigtem Zahlungseingang</b> fest in unseren Kalender
                    eingetragen –
                    entweder nach Geldeingang auf unserem Konto oder nach Zusendung eines Zahlungsnachweises
                    (z. B. Screenshot).
                    <br><br>
                    Bei rechtzeitiger Terminverschiebung finden wir immer eine faire Lösung.
                </div>
            </details>

            <details class="faq-item">
                <summary>Was kostet ein Tattoo?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Der Preis hängt von <b>Größe, Stil, Detailgrad</b> und der Körperstelle ab.
                    Kleinere Fineline- oder Dotwork-Motive starten bei unserem Mindestpreis.
                    Für größere Projekte kalkulieren wir transparent nach tatsächlichem Aufwand.
                    <br><br>
                    Wir liegen preislich im <b>mittleren Segment</b>, liefern aber Qualität im oberen Bereich –
                    dank inhabergeführtem Studio ohne unnötige Zwischenkosten.
                </div>
            </details>

            <details class="faq-item">
                <summary>Erstellt ihr wirklich individuelle Designs?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Jedes Motiv entsteht bei uns zu <b>100 % individuell</b> – keine Vorlagen aus dem Internet,
                    keine
                    Kopien.
                    <br><br>
                    Wir arbeiten in zwei Phasen:
                    <ul>
                        <li><b>Basisdesign</b> vor dem Termin – alle wichtigen Elemente sind vorbereitet.</li>
                        <li><b>Feinanpassung</b> direkt am Termin – so fließen deine letzten Ideen mit ein.</li>
                    </ul>
                    Entwürfe zeigen wir <b>nur im Studio</b>, um kreative Qualität und Schutz unserer Arbeit zu
                    gewährleisten.
                </div>
            </details>

            <details class="faq-item">
                <summary>Macht ihr Cover-ups oder Reworks?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Ja – in ca. <b>90 % der Fälle</b> sind erfolgreiche Cover-ups möglich, sofern das alte Tattoo
                    nicht
                    zu dunkel oder großflächig schwarz ist.
                    Wir arbeiten in einer bewährten <b>Gray-Pastel-Technik</b> für harmonische, langlebige
                    Ergebnisse.
                    <br><br>
                    Falls nötig, empfehlen wir vorab eine Laser-Aufhellung – immer mit ehrlicher Beratung.
                </div>
            </details>

            <details class="faq-item">
                <summary>Tut ein Tattoo weh?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Schmerzempfinden ist individuell und hängt stark von der Körperstelle ab.
                    Dank moderner Maschinen, präziser Technik und ruhiger Arbeitsweise empfinden viele Kund*innen
                    den
                    Ablauf bei uns als <b>deutlich angenehmer</b> als erwartet.
                    Regelmäßige Pausen sorgen zusätzlich für Komfort.
                </div>
            </details>

            <details class="faq-item">
                <summary>Wie pflege ich mein Tattoo richtig?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Wir nutzen erprobte Methoden aus jahrelanger Erfahrung:
                    <ul>
                        <li>Sauber halten, dünn pflegen.</li>
                        <li>Keine Sauna, Sonne oder Schwimmbad in der Heilungsphase.</li>
                        <li>Kein Kratzen oder Reiben.</li>
                    </ul>
                    Für kleine Tattoos nutzen wir oft eine <b>Schutzfolie für 3–4 Tage</b>.
                    Danach reicht meist eine dünne Pflege mit <i>Bepanthen</i>.
                    Bei mittleren und großen Tattoos geben wir dir eine individuelle Aftercare-Anleitung mit.
                </div>
            </details>

            <details class="faq-item">
                <summary>Mit welchen Stilen arbeitet ihr?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Unsere Spezialitäten: <b>Fineline, Black & Gray, Dotwork, Mandala, Single-Needle, Floral und
                        Chicano</b>.
                    Auf Wunsch setzen wir auch andere Stile um, sofern Qualität und Langlebigkeit gewährleistet
                    sind.
                </div>
            </details>

            <details class="faq-item">
                <summary>Tätowiert ihr auch unter 18?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Wir empfehlen ein Mindestalter von 18 Jahren.
                    Ab 16 Jahren ist ein Tattoo nur nach persönlicher Beratung und mit schriftlicher Zustimmung
                    <b>beider Erziehungsberechtigten</b> möglich.
                </div>
            </details>

            <details class="faq-item">
                <summary>Wie lange dauert ein Termin?<span class="arrow">▾</span></summary>
                <div class="faq-answer">
                    Kleine Motive: ca. 30–90 Minuten.
                    Mittlere Motive: 2–4 Stunden.
                    Große Motive: werden in einer oder mehreren Tagessitzungen umgesetzt.
                    <br><br>
                    Tagessitzungen starten um <b>10:00&nbsp;Uhr</b> und gehen in der Regel bis maximal
                    <b>18:00&nbsp;Uhr</b>.
                    In besonderen Fällen kann – je nach Projekt – auch eine Verlängerung bis <b>20:00&nbsp;Uhr</b>
                    vereinbart werden.
                </div>
            </details>

